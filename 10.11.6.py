import itertools as it

def ranges(numbers :list):
   my_grouping = it.groupby(numbers,key = lambda x: numbers.index(x)-x)
   result = list()
   for i ,gr in my_grouping:
       mt = tuple(gr)
       result.append((min(mt),max(mt)))
   
   return result
       
       
