import csv


raw_file = 'student_counts.csv'


with open (raw_file,'r',encoding='utf8',newline='') as file_open:

    file_read = list(csv.DictReader(file_open, delimiter= ','))
    
    header = list(file_read[0].keys())
    
    h = ['year']+sorted(header[1:],key = lambda x: (int(x.split('-')[0]), x.split('-')[1]))
    
with open ('sorted_student_counts.csv','w',encoding = 'utf8',newline='') as write_file:
    writer = csv.DictWriter(write_file,fieldnames = h,delimiter=',')
    writer.writeheader()
    for row in file_read:
        writer.writerow(row)

